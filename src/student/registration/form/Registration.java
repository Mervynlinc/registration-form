    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
//             query = "select * from students ";
//            resultset = statement.executeQuery(query);
            
            String regno = txtregno.getText(); 
            String fname = txtfname.getText();
            String lname = txtlname.getText(); 
            String sex = "";
            Date date = txtdate.getDate() ;
                    
            //converting date to be formatted and stored as a string 
            SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
            String strDate = formatter.format(date);
            
            
            if (txtmale.isSelected()){
                sex = "M";
            }
            
            else{
                sex = "F";
            }
               if (fname.isEmpty()){
                   JOptionPane.showMessageDialog(null, "Enter your first name");
                   txtfname.requestFocus();
               }   
               else if(lname.isEmpty()){
                    JOptionPane.showMessageDialog(null, "Enter your last  name");
                    txtlname.requestFocus();
               }
               else if(regno.isEmpty()){
                   JOptionPane.showMessageDialog(null, "Enter your regno");
                    txtregno.requestFocus();
               }
               else if(sex.isEmpty()){
                   JOptionPane.showMessageDialog(null, "select your gender");
                   
               }
               else if(txtdate.getDate() == null){
                   JOptionPane.showMessageDialog(null, "Enter your birthday");
                   txtdate.requestFocus();
               }
            
               else{
                   try{
                       query = "insert into students(regno, first_name, last_name, sex, date_of_birth, image) VALUES('"+regno+"' ,'"+fname+"' ,'"+lname+"' ,'"+sex+"' ,'"+strDate+"', '"+picturepath+"'  )";
                       statement.executeUpdate(query);
                       JOptionPane.showMessageDialog(null, "record saved");
                       txtregno.setText("");
                       txtfname.setText("");
                       txtlname.setText("");
                       picturepath = "";
                       txtimagepath.setIcon(new ImageIcon(picturepath));
                       txtsex.clearSelection();
                       txtdate.setDate(null);
                       txtregno.setText("");
                       txtimagepath.setText("");
                       txtfname.requestFocus();
                         
                resultset = statement.executeQuery("SELECT * FROM STUDENTS");
                       
                   }
                   catch(SQLException ex)
                   {
                       ex.printStackTrace();
                   }
               }
    }//GEN-LAST:event_jButton1ActionPerformed

