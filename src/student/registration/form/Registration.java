    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         String imagepath = "";                         //initialise image path as a string
         JFileChooser chooser = new JFileChooser();     //create a file chooser 
         int choice = chooser.showOpenDialog(null);     //opens a jfile chooser dialogebox
         
         if (choice == JFileChooser.APPROVE_OPTION){    //if  we press ok on the file chooser 
             File file = chooser.getSelectedFile();     //file ssaves our chosen file 
             imagepath = file.getAbsolutePath();        //method gets the path of the chosen file and sacves it in the variable imagepath
             imagepath = imagepath.replace("\\", "/");  // finds "\\" and replaces it with /
         }
         ImageIcon icon = new  ImageIcon(imagepath);
         Image image = icon.getImage().getScaledInstance(txtimagepath.getWidth(), txtimagepath.getHeight(), Image.SCALE_SMOOTH); //auto scales the image
         txtimagepath.setIcon(new ImageIcon(image));
         picturepath = imagepath;
      
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
           String regno = txtregno.getText(); 
           
        try{  
                 
                if(txtregno.getText().isEmpty()){
                     JOptionPane.showMessageDialog(null, "Please insert the regno for te record you want to delete");
                     txtregno.requestFocus();
                 }
                else{
                 query = "delete from students where regno = '" +regno+ "' ";
                 int response = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this record?", "confirm", JOptionPane.YES_NO_CANCEL_OPTION);
                 if(response == JOptionPane.YES_OPTION){
                     JOptionPane.showMessageDialog(null, "record deleted");
                    statement.executeUpdate(query);
                 }
                 if(response == JOptionPane.NO_OPTION){
                     JOptionPane.showMessageDialog(null, "operation canceled");
                    
                 }
                 resultset = statement.executeQuery("SELECT * FROM STUDENTS");
             }
        }
       catch(SQLException ex)
           {
               ex.printStackTrace();
           }
               
                
    }//GEN-LAST:event_jButton2ActionPerformed

